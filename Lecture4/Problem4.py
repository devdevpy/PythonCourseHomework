num = int(input())

if num == 0:
    print("Нула")
elif num == 1:
    print("Едно")
elif num == 2:
    print("Две")
elif num == 3:
    print("Три")
elif num == 4:
    print("Четири")
elif num == 5:
    print("Пет")
elif num == 6:
    print("Шест")
elif num == 7:
    print("Седем")
elif num == 8:
    print("Осем")
elif num == 9:
    print("Девет")
else:
    print("Невалидно число")
